int sumN_com(int* a, int n)
{
	int sum = 0;
	for (int i = 0; i < n; i++)
	{
		sum += a[i];
	}
	return sum;
}
int sumN_opt2(int* a, int n)
{
	int sum1 = 0, sum2 = 0;
	for (int i = 0; i < n; i += 2)
	{
		sum1 += a[i];
		if (i != n - 1)
		{
			sum2 += a[i + 1];
		}
	}
	return sum1 + sum2;
}
