int sumN_recursion(int* a, int n)
{
	for (int m = n; m > 1; m /= 2)
	{
		for (int i = 0; i < m / 2; i++)
		{
			a[i] = a[i * 2] + a[i * 2 + 1];
		}
	}
	return a[0];
}
